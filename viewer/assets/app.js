const PASSWORD_CACHE_PREFIX = "sheetshare-mobile:v1";
const ENCRYPTED_SNAPSHOT_SCHEMA = "sheetshare-mobile.encrypted-snapshot.v1";
const TRUSTED_SNAPSHOT_SCHEMA = "sheetshare-mobile.trusted-snapshot.v1";

let activeLanguage = resolveInitialLanguage();
applyDocumentLanguage(activeLanguage);

function t(key, replacements = {}, language = activeLanguage) {
  const dictionary = VIEWER_I18N[language] || VIEWER_I18N.en;
  const fallback = VIEWER_I18N.en;
  const value = lookup(dictionary, key) ?? lookup(fallback, key) ?? key;
  return String(value).replace(/\{(\w+)\}/g, (_match, name) => replacements[name] ?? "");
}

function lookup(object, path) {
  return String(path).split(".").reduce((current, part) => current?.[part], object);
}

function localizedLookup(path) {
  const dictionary = VIEWER_I18N[activeLanguage] || VIEWER_I18N.en;
  return lookup(dictionary, path) ?? lookup(VIEWER_I18N.en, path);
}

function localizedAbilityLabel(ability) {
  const key = String(ability?.key || "").toLowerCase();
  return key ? (localizedLookup(`abilities.${key}`) || ability.label || key) : (ability?.label || "");
}

function localizedSkillLabel(skill) {
  const key = String(skill?.key || "").toLowerCase();
  return key ? (localizedLookup(`skillLabels.${key}`) || skill.label || key) : (skill?.label || "");
}

function localizedListLabel(row, labelType) {
  if (labelType === "ability") return localizedAbilityLabel(row);
  if (labelType === "skill") return localizedSkillLabel(row);
  return row?.label || row?.name || row?.key || "";
}

function resolveInitialLanguage() {
  const params = new URLSearchParams(window.location.search);
  const explicit = params.get("lang");
  if (explicit) return normalizeLanguage(explicit);
  const browserLanguages = navigator.languages?.length ? navigator.languages : [navigator.language];
  return normalizeLanguage(browserLanguages.find(Boolean));
}

function resolveEnvelopeLanguage(envelope) {
  const params = new URLSearchParams(window.location.search);
  const explicit = params.get("lang");
  if (explicit) return normalizeLanguage(explicit);
  const language = envelope?.language;
  if (language?.url) return normalizeLanguage(language.url);
  if (language?.mode === "foundry") return normalizeLanguage(language.foundry);
  return activeLanguage;
}

function normalizeLanguage(value) {
  const text = String(value || "").toLowerCase();
  if (text.startsWith("zh") || text.startsWith("cn")) return "zh-CN";
  return "en";
}

function applyDocumentLanguage(language) {
  document.documentElement.lang = language;
  document.title = VIEWER_I18N[language]?.documentTitle || VIEWER_I18N.en.documentTitle;
}

window.characterSheetViewer = function characterSheetViewer() {
  return {
    uiLanguage: activeLanguage,
    worldTitle: t("brand"),
    envelope: null,
    selected: null,
    error: "",
    unlockError: "",
    password: "",
    needsPassword: true,
    externalAuth: resolveViewerMode() === "external",
    unlocking: false,
    listQuery: "",
    spellQuery: "",
    itemQuery: "",
    spellLevel: "all",
    itemFilter: "all",
    tab: "overview",
    pollTimer: null,
    snapshotBase: "",
    tabs: [
      { id: "overview" },
      { id: "spells" },
      { id: "actions" },
      { id: "inventory" },
      { id: "features" }
    ],

    t(key, replacements = {}) {
      return t(key, replacements, this.uiLanguage);
    },

    tabLabel(tab) {
      return this.t(`tabs.${tab.id}`);
    },

    abilityLabel(ability) {
      return localizedAbilityLabel(ability);
    },

    setLanguage(language) {
      activeLanguage = normalizeLanguage(language);
      this.uiLanguage = activeLanguage;
      applyDocumentLanguage(activeLanguage);
    },

    async init() {
      try {
        this.snapshotBase = resolveSnapshotBase();
        await this.loadEnvelope();
      } catch (error) {
        this.error = error.message || String(error);
      }
    },

    async loadEnvelope() {
      const slug = resolveSlug();
      const response = await fetch(`${this.snapshotBase}${slug}.json?ts=${Date.now()}`, { cache: "no-store" });
      if (!response.ok) throw new Error(this.t("fetchMissing"));
      this.envelope = await response.json();
      if (this.externalAuth) {
        if (this.envelope?.schema !== TRUSTED_SNAPSHOT_SCHEMA) {
          throw new Error(this.t("externalAuthFormatMismatch"));
        }
        this.setLanguage(resolveEnvelopeLanguage(this.envelope));
        this.applySnapshot(this.envelope);
        return;
      }
      if (this.envelope?.schema !== ENCRYPTED_SNAPSHOT_SCHEMA) {
        throw new Error(this.t("unsupportedFormat"));
      }
      this.setLanguage(resolveEnvelopeLanguage(this.envelope));
      this.worldTitle = this.t("enterPasswordTitle");
      await this.tryCachedUnlock();
    },

    filteredActors() {
      return [];
    },

    async unlock() {
      this.unlockError = "";
      if (!this.password) {
        this.unlockError = this.t("missingPassword");
        return;
      }
      this.unlocking = true;
      try {
        const snapshot = await decryptEnvelope(this.envelope, this.password);
        saveCachedPassword(this.password, this.envelope);
        this.applySnapshot(snapshot);
      } catch (error) {
        this.unlockError = this.t("wrongPassword");
      } finally {
        this.unlocking = false;
      }
    },

    async tryCachedUnlock() {
      const cachedPassword = loadCachedPassword();
      if (!cachedPassword) return;
      this.unlocking = true;
      try {
        this.password = cachedPassword;
        const snapshot = await decryptEnvelope(this.envelope, cachedPassword);
        this.applySnapshot(snapshot);
      } catch (error) {
        clearCachedPassword();
        this.password = "";
        this.unlockError = this.t("wrongPassword");
      } finally {
        this.unlocking = false;
      }
    },

    applySnapshot(snapshot) {
      this.selected = snapshot;
      this.worldTitle = snapshot?.world?.title || this.t("characterSheet");
      this.needsPassword = false;
      this.unlockError = "";
      this.tab = "overview";
      this.spellQuery = "";
      this.itemQuery = "";
      this.spellLevel = "all";
      this.itemFilter = "all";
      this.startPolling();
      window.scrollTo({ top: 0, behavior: "instant" });
    },

    showList() {
      this.stopPolling();
      clearCachedPassword();
      this.selected = null;
      this.error = "";
      this.unlockError = "";
      this.needsPassword = !this.externalAuth;
      this.password = "";
      window.scrollTo({ top: 0, behavior: "instant" });
    },

    async fetchSnapshot() {
      const slug = resolveSlug();
      const response = await fetch(`${this.snapshotBase}${slug}.json?ts=${Date.now()}`, { cache: "no-store" });
      if (!response.ok) throw new Error(this.t("readFailed"));
      this.envelope = await response.json();
      if (this.externalAuth) {
        if (this.envelope?.schema !== TRUSTED_SNAPSHOT_SCHEMA) throw new Error(this.t("externalAuthFormatMismatch"));
        return this.envelope;
      }
      try {
        return await decryptEnvelope(this.envelope, this.password);
      } catch (error) {
        const decryptError = new Error("Sheet decrypt failed");
        decryptError.cause = error;
        decryptError.sheetshareDecryptFailed = true;
        throw decryptError;
      }
    },

    startPolling() {
      this.stopPolling();
      this.pollTimer = window.setInterval(async () => {
        if (document.hidden) return;
        try {
          const next = await this.fetchSnapshot();
          if (!this.selected || next.contentHash !== this.selected.contentHash) this.selected = next;
        } catch (error) {
          if (error?.sheetshareDecryptFailed) {
            clearCachedPassword();
            this.password = "";
            this.selected = null;
            this.needsPassword = true;
            this.unlockError = this.t("wrongPassword");
            this.stopPolling();
            return;
          }
          console.warn("Character sheet refresh failed", error);
        }
      }, 10000);
    },

    stopPolling() {
      if (this.pollTimer) window.clearInterval(this.pollTimer);
      this.pollTimer = null;
    },

    filteredSpells() {
      const spells = this.selected?.sections?.spells ?? [];
      const query = this.spellQuery.trim().toLowerCase();
      return spells.filter(spell => {
        const levelMatch = this.spellLevel === "all" || String(spell.level) === this.spellLevel;
        const text = [spell.name, spell.school, spell.components, stripHtml(spell.descriptionHtml)].join(" ").toLowerCase();
        return levelMatch && (!query || text.includes(query));
      });
    },

    spellLevels() {
      return [...new Set((this.selected?.sections?.spells ?? []).map(spell => spell.level))].sort((a, b) => a - b);
    },

    spellLevelLabel(level) {
      return spellLevelLabel(level);
    },

    filteredItems() {
      const items = this.selected?.sections?.inventory ?? [];
      const query = this.itemQuery.trim().toLowerCase();
      return items.filter(item => {
        if (this.itemFilter === "equipped" && !item.equipped) return false;
        if (this.itemFilter !== "all" && this.itemFilter !== "equipped" && item.type !== this.itemFilter) return false;
        const text = [item.name, item.type, item.attunementLabel, stripHtml(item.descriptionHtml)].join(" ").toLowerCase();
        return !query || text.includes(query);
      });
    },

    spellSlotLabel(slot) {
      return spellSlotLabel(slot);
    },

    spellSlotAvailable(slot) {
      return spellSlotAvailable(slot);
    },

    spellSlotCells(slot) {
      const max = Math.max(Number(slot?.max ?? 0), 0);
      const available = spellSlotAvailable(slot);
      return Array.from({ length: max }, (_, index) => ({
        index,
        spent: index >= available
      }));
    },

    spellSlotSummary() {
      const slots = this.selected?.resources?.spellSlots ?? [];
      const available = slots.reduce((sum, slot) => sum + spellSlotAvailable(slot), 0);
      const max = slots.reduce((sum, slot) => sum + Math.max(Number(slot.max ?? 0), 0), 0);
      return this.t("availableSummary", { available, max });
    },

    specialResources() {
      return specialResourcesFor(this.selected);
    },

    resourceAvailable(resource) {
      return getResourceAvailable(resource);
    },

    resourceMax(resource) {
      return getResourceMax(resource);
    },

    resourcePct(resource) {
      const max = getResourceMax(resource);
      return max > 0 ? Math.max(0, Math.min(100, Math.round((getResourceAvailable(resource) / max) * 100))) : 0;
    },

    tempHpPct() {
      const hp = this.selected?.resources?.hp ?? {};
      const exportedPct = Number(hp.tempPct);
      if (Number.isFinite(exportedPct) && exportedPct > 0) return exportedPct;
      const temp = Number(hp.temp ?? 0);
      if (!Number.isFinite(temp) || temp <= 0) return 0;
      const max = Math.max(Number(hp.max ?? 0), temp);
      return max > 0 ? Math.max(8, Math.min(100, Math.round((temp / max) * 100))) : 0;
    },

    resistanceItems() {
      return normalizeStringList(this.selected?.details?.traits?.damageResistances);
    },

    resistanceSummary() {
      return compactTraitSummary(this.resistanceItems(), this.selected?.summary?.resistances);
    },

    resistanceTitle() {
      return traitFullText(this.resistanceItems(), this.selected?.summary?.resistances);
    },

    resourceMeta(resource) {
      return [resource.recovery, resource.activation].filter(Boolean).join(" / ");
    },

    questPanelUrl() {
      return questPanelUrl();
    },

    initials(name) {
      return String(name || "?").split(/\s+/).map(part => part[0]).slice(0, 2).join("").toUpperCase();
    },

    assetUrl(path) {
      if (!path) return "";
      if (/^(https?:|data:|blob:)/i.test(path)) return path;
      return `/${String(path).replace(/^\/+/, "")}`;
    },

    formatUpdated(value) {
      if (!value) return this.t("notUpdated");
      const date = new Date(value);
      if (Number.isNaN(date.getTime())) return this.t("justNow");
      const seconds = Math.max(0, Math.round((Date.now() - date.getTime()) / 1000));
      if (seconds < 60) return this.t("justNow");
      if (seconds < 3600) return this.t("minutesAgo", { count: Math.floor(seconds / 60) });
      if (seconds < 86400) return this.t("hoursAgo", { count: Math.floor(seconds / 3600) });
      return date.toLocaleDateString(activeLanguage);
    },

    listPanelHtml(title, rows, valueKey, labelType = "") {
      const safeRows = rows ?? [];
      if (!safeRows.length) return "";
      const displayRows = labelType === "skill"
        ? [...safeRows].sort((a, b) => localizedListLabel(a, labelType).localeCompare(localizedListLabel(b, labelType), activeLanguage))
        : safeRows;
      return `
        <div class="panel">
          <div class="panel-head"><h3>${escapeHtml(title)}</h3></div>
          <div class="panel-body">
            <div class="compact-list">
              ${displayRows.map(row => `
                <div class="line-item">
                  <span class="line-name">${escapeHtml(localizedListLabel(row, labelType))}</span>
                  <span class="line-meta">${escapeHtml(row[valueKey] ?? row.value ?? "")}</span>
                </div>
              `).join("")}
            </div>
          </div>
        </div>
      `;
    },

    traitPanelHtml(title, items) {
      const safeItems = items ?? [];
      if (!safeItems.length) return "";
      return `
        <div class="panel">
          <div class="panel-head"><h3>${escapeHtml(title)}</h3></div>
          <div class="panel-body">
            <div class="pill-row">${safeItems.map(item => `<span class="pill">${escapeHtml(item)}</span>`).join("")}</div>
          </div>
        </div>
      `;
    },

    detailsListHtml(items, kind) {
      const safeItems = items ?? [];
      if (!safeItems.length) return `<div class="empty-state"><strong>${escapeHtml(t("empty"))}</strong></div>`;
      return `
        <div class="panel">
          <div class="panel-head"><h3>${kindTitle(kind)}</h3><span class="pill">${safeItems.length}</span></div>
          <div class="panel-body">
            ${safeItems.map(item => `
              <details>
                <summary>
                  ${escapeHtml(item.name)}
                  <span class="detail-meta">${escapeHtml(itemMeta(item, kind))}</span>
                </summary>
                <div class="detail-copy">
                  ${detailFacts(item, kind)}
                  ${item.descriptionHtml || ""}
                </div>
              </details>
            `).join("")}
          </div>
        </div>
      `;
    },

    actionGroupsHtml(items) {
      const safeItems = items ?? [];
      if (!safeItems.length) return `<div class="empty-state"><strong>${escapeHtml(t("empty"))}</strong></div>`;
      const groups = [
        { key: "action", title: t("standardAction") },
        { key: "bonus", title: t("bonusAction") },
        { key: "reaction", title: t("reaction") },
        { key: "special", title: t("specialOther") }
      ].map(group => ({
        ...group,
        items: safeItems.filter(item => actionGroupKey(item.activation) === group.key)
      })).filter(group => group.items.length);

      return `<div class="action-groups">
        ${groups.map(group => `
          <div class="panel">
            <div class="panel-head"><h3>${escapeHtml(group.title)}</h3><span class="pill">${group.items.length}</span></div>
            <div class="panel-body">
              ${group.items.map(item => detailItemHtml(item, "action")).join("")}
            </div>
          </div>
        `).join("")}
      </div>`;
    }
  };
};

window.addEventListener("popstate", () => {
  window.location.reload();
});

function kindTitle(kind) {
  return {
    spell: t("spellsTitle"),
    action: t("actionsTitle"),
    item: t("itemsTitle"),
    feature: t("featuresTitle")
  }[kind] || t("contentTitle");
}

function itemMeta(item, kind) {
  if (kind === "spell") return [spellLevelLabel(item.level), item.concentration ? t("concentration") : "", item.ritual ? t("ritual") : ""].filter(Boolean).join(" / ");
  if (kind === "item") return [item.equipped ? t("equipped") : "", item.type, item.uses].filter(Boolean).join(" / ");
  if (kind === "action") return [actionActivationLabel(item.activation), item.attackBonus, item.damage, item.saveDc ? `DC ${item.saveDc}` : "", item.uses].filter(Boolean).join(" / ");
  return [item.source, item.uses].filter(Boolean).join(" / ");
}

function detailItemHtml(item, kind) {
  return `
    <details>
      <summary>
        ${escapeHtml(item.name)}
        <span class="detail-meta">${escapeHtml(itemMeta(item, kind))}</span>
      </summary>
      <div class="detail-copy">
        ${detailFacts(item, kind)}
        ${item.descriptionHtml || ""}
      </div>
    </details>
  `;
}

function detailFacts(item, kind) {
  const facts = [];
  if (kind === "spell") {
    facts.push([t("casting"), item.activation], [t("range"), item.range], [t("target"), item.target], [t("duration"), item.duration], [t("components"), item.components], ["DC", item.saveDc]);
  } else if (kind === "item") {
    facts.push([t("quantity"), item.quantity], [t("attunement"), item.attunementLabel], [t("weight"), item.weight], [t("price"), item.price], [t("uses"), item.uses]);
  } else {
    facts.push([t("activation"), item.activation], [t("range"), item.range], ["DC", item.saveDc], [t("save"), item.saveAbility], [t("uses"), item.uses]);
  }
  const html = facts
    .filter(([, value]) => value !== null && value !== undefined && value !== "")
    .map(([label, value]) => `<span class="pill">${escapeHtml(label)} ${escapeHtml(value)}</span>`)
    .join("");
  return html ? `<div class="pill-row">${html}</div>` : "";
}

function actionGroupKey(activation) {
  const text = String(activation || "").toLowerCase();
  if (text.includes("bonus")) return "bonus";
  if (text.includes("reaction")) return "reaction";
  if (text.includes("action")) return "action";
  return "special";
}

function actionActivationLabel(activation) {
  const group = actionGroupKey(activation);
  return {
    action: t("standardAction"),
    bonus: t("bonusAction"),
    reaction: t("reaction"),
    special: activation ? t("special") : ""
  }[group];
}

function spellSlotAvailable(slot) {
  return Math.max(Number(slot?.available ?? slot?.value ?? 0), 0);
}

function spellSlotLabel(slot) {
  const level = Number(slot?.level ?? 0);
  const label = spellLevelLabel(level);
  return label || slot?.label || t("spellLevel", { level });
}

function spellLevelLabel(level) {
  const numeric = Number(level);
  if (numeric === 0) return t("cantrip");
  return t("spellLevel", { level: numeric });
}

function specialResourcesFor(snapshot) {
  const structured = snapshot?.resources?.special ?? snapshot?.resources?.specialResources ?? [];
  const fallback = fallbackSpecialResources(snapshot);
  return mergeSpecialResources([...structured, ...fallback]);
}

function fallbackSpecialResources(snapshot) {
  const items = [
    ...(snapshot?.sections?.features ?? []),
    ...(snapshot?.sections?.actions ?? [])
  ];
  return items
    .map(item => {
      const parsed = parseUsesText(item.uses);
      const known = knownSpecialResource(item.name);
      if (!parsed && !known) return null;
      const max = parsed?.max || known?.max || 0;
      if (max <= 0) return null;
      const spent = Math.max(parsed?.spent ?? 0, 0);
      return {
        id: item.id ? `fallback:${item.id}` : `fallback:${item.name}`,
        name: item.name,
        value: Math.max(max - spent, 0),
        max,
        spent,
        recovery: parsed?.recovery || known?.recovery || "",
        activation: item.activation || "",
        source: item.source || ""
      };
    })
    .filter(Boolean);
}

function normalizeStringList(value) {
  if (!value) return [];
  const values = Array.isArray(value) ? value : String(value).split(/[;,/]/);
  return values.map(item => String(item || "").trim()).filter(Boolean);
}

function summarizeStringList(items = []) {
  const values = normalizeStringList(items);
  if (!values.length) return "";
  if (values.length <= 2) return values.join(" / ");
  return `${values.slice(0, 2).join(" / ")} +${values.length - 2}`;
}

function compactTraitSummary(items = [], fallback = "") {
  const values = normalizeStringList(items?.length ? items : fallback);
  if (!values.length) return "";
  const joined = summarizeStringList(values);
  if (values.length === 1 && joined.length <= 8) return joined;
  if (values.length <= 2 && joined.length <= 8 && values.every(value => value.length <= 4)) return joined;
  return t("traitCount", { count: values.length });
}

function traitFullText(items = [], fallback = "") {
  const values = normalizeStringList(items?.length ? items : fallback);
  return values.join(" / ");
}

function parseUsesText(value) {
  const text = String(value || "").trim();
  if (!text) return null;
  const match = text.match(/(\d+)\s*\/\s*(\d+)(?:\s*\(([^)]+)\))?/);
  if (!match) return null;
  const current = Number(match[1]);
  const max = Number(match[2]);
  if (!Number.isFinite(max) || max <= 0) return null;
  return {
    value: Number.isFinite(current) ? current : 0,
    max,
    spent: Number.isFinite(current) ? Math.max(max - current, 0) : 0,
    recovery: match[3] || ""
  };
}

function knownSpecialResource(name) {
  const text = String(name || "");
  const isChannelDivinity = /(^|\s)(引导神力|Channel Divinity)(\s|$)/i.test(text)
    && !/引导神力[:：]|Channel Divinity:/i.test(text);
  if (isChannelDivinity) return { max: 1, recovery: t("shortRest") };
  return null;
}

function mergeSpecialResources(resources) {
  const byKey = new Map();
  for (const resource of resources) {
    if (!resource?.name || getResourceMax(resource) <= 0) continue;
    const key = normalizeResourceKey(resource.name);
    const current = byKey.get(key);
    if (!current || getResourceMax(resource) > getResourceMax(current) || getResourceAvailable(resource) > getResourceAvailable(current)) {
      byKey.set(key, resource);
    }
  }
  return Array.from(byKey.values()).sort((a, b) => String(a.name).localeCompare(String(b.name), activeLanguage));
}

function normalizeResourceKey(name) {
  return String(name || "").replace(/\s+/g, " ").trim().toLowerCase();
}

function getResourceAvailable(resource) {
  const value = Number(resource?.value);
  if (Number.isFinite(value)) return Math.max(value, 0);
  const max = getResourceMax(resource);
  const spent = Number(resource?.spent ?? 0);
  return Math.max(max - (Number.isFinite(spent) ? spent : 0), 0);
}

function getResourceMax(resource) {
  const max = Number(resource?.max);
  return Number.isFinite(max) ? Math.max(max, 0) : 0;
}

